// Пакет ai содержит промпты и схему ответа для LLM-генератора мутантов.
package ai

import "fmt"

// SystemPrompt — системная инструкция для LLM-сессии.
// Определяет роль модели и формат ответа.
const SystemPrompt = `Ты — движок мутационного тестирования. Твоя задача — вносить
реалистичные логические ошибки в исходный код на Go, похожие на ошибки
разработчика при реализации требований, рефакторинге или обработке edge case,
чтобы оценить качество тестового набора.
Ты всегда отвечаешь строго в соответствии с заданной JSON-схемой и больше ничего не пишешь.`

// MutationJSONSchema — JSON Schema для Structured Output.
// Описывает структуру ответа, которую LLM должна вернуть.
// Используется с моделями, поддерживающими режим Structured Output.
var MutationJSONSchema = map[string]any{
	"type": "object",
	"properties": map[string]any{
		"operator_name": map[string]any{
			"type":        "string",
			"description": "Короткое название оператора мутации, например OffByOneError",
		},
		"description": map[string]any{
			"type":        "string",
			"description": "Одно предложение, объясняющее внесённый дефект. Если дефект показывает пробел в тесте, назови этот пробел.",
		},
		"behavioral_impact": map[string]any{
			"type":        "string",
			"description": "Конкретный пример входных данных, на которых мутированная версия вернёт ДРУГОЙ результат, чем оригинал. Например: 'Add(2,3) вернёт 1 вместо 5'. Если такого примера нет — мутация эквивалентна и её нельзя использовать.",
		},
		"original_snippet": map[string]any{
			"type":        "string",
			"description": "Точная и по возможности уникальная подстрока из исходного кода, которую нужно заменить",
		},
		"mutated_snippet": map[string]any{
			"type":        "string",
			"description": "Текст замены — синтаксически корректный Go-код",
		},
	},
	"required":             []string{"operator_name", "description", "behavioral_impact", "original_snippet", "mutated_snippet"},
	"additionalProperties": false,
}

// BuildPrompt формирует Chain-of-Thought промпт для LLM.
// Модель должна:
//  1. Проанализировать логику функции.
//  2. Выбрать одну семантически значимую точку мутации.
//  3. Вернуть структурированный ответ в формате JSON.
//
// Параметр useStructuredOutput указывает, ожидается ли ответ через
// Structured Output API (тогда JSON-инструкции в промпте не нужны).
// testBody — исходный код теста, покрывающего функцию (может быть пустым).
func BuildPrompt(funcBody, fileName string, lineHint int, useStructuredOutput bool, testBody string) string {
	formatInstructions := ""
	if !useStructuredOutput {
		// При обычном текстовом режиме явно задаём формат ответа в промпте
		formatInstructions = `
## Формат ответа
Верни ТОЛЬКО валидный JSON без markdown-разметки:
{
  "operator_name": "<название оператора>",
  "description": "<одно предложение об ошибке и, если применимо, о пробеле в тесте>",
  "behavioral_impact": "<конкретный пример входа, где мутант вернёт другой результат>",
  "original_snippet": "<точная подстрока для замены>",
  "mutated_snippet": "<текст замены>"
}
`
	}

	testSection := ""
	if testBody != "" {
		testSection = fmt.Sprintf(`
## Тест, покрывающий функцию
Используй этот тест как контекст ожидаемого поведения и текущего покрытия.
Не подгоняй мутацию под конкретные assert-ы теста и не выбирай искусственную
замену только ради того, чтобы тест её пропустил.
Сначала выбери правдоподобную логическую ошибку в реализации функции.
Если из теста видно, что он не проверяет важный edge case, ошибочную ветку,
nil/empty distinction, граничное значение или альтернативный сценарий,
сгенерируй мутанта, который ломает именно это непроверенное поведение и
укажи пробел проверки в description или behavioral_impact.
%s
`, testBody)
	}

	return fmt.Sprintf(`Ты — эксперт по тестированию программного обеспечения на Go.

Сгенерируй ОДНУ реалистичную мутацию для функции ниже.
Мутация должна имитировать реальную логическую ошибку разработчика в написании программы,
а не случайную замену токена.

## Правила
- Мутируй ТОЛЬКО ОДНО место в функции.
- Не добавляй импорты и не меняй сигнатуру функции.
- "original_snippet" — это точная, по возможности уникальная подстрока, присутствующая в коде.
- "mutated_snippet" — валидный Go-код, замещающий original_snippet.
- Предпочитай логические ошибки, которые реально встречаются в коде:
  неверное граничное условие; перепутанный && и ||; пропущенная или лишняя проверка;
  неверный default/fallback/error path; перепутанное поле, переменная, аргумент или
  константа того же смысла; неверный порядок, индекс, диапазон, приоритет или направление сравнения.
- Избегай искусственных значений, debug/test-суффиксов, бессмысленных литералов и грубых
  замен вида "return nil", "return 0", "return false", если такая замена не выглядит как
  правдоподобная ошибка в этой функции.
- ЗАПРЕЩЕНО генерировать семантически эквивалентные мутации.
  Эквивалентная мутация — та, при которой функция возвращает тот же результат
  на ВСЕХ возможных входных данных. Например, в функции Max(a,b) замена
  "a > b" на "a >= b" эквивалентна, потому что при a==b оба варианта
  возвращают одинаковое значение.
- Перед генерацией мысленно проверь: существует ли хотя бы один конкретный
  входной набор, на котором мутированная версия вернёт ДРУГОЙ результат?
  Если нет — выбери другое место для мутации.
%s%s
## Функция для мутации
// Файл: %s  (область — строка %d)
%s
`, formatInstructions, testSection, fileName, lineHint, funcBody)
}
