// Пакет ai содержит промпты и схему ответа для LLM-генератора мутантов.
package ai

import (
	"fmt"
	"strings"
)

// SystemPrompt — короткая системная инструкция. Определяет роль модели
// и обещание возвращать строго JSON по схеме.
//
// Сама chain-of-thought-логика выбора мутации задана в BuildPrompt,
// чтобы её было проще менять без сброса роли.
const SystemPrompt = `Ты — движок мутационного тестирования Go-кода.
Твоя цель — оценивать качество тестового набора через генерацию реалистичных,
доменно осмысленных мутаций. Ты:
1) сначала разбираешься в коде — его контексте, предметной области и смысле;
2) если есть тест — анализируешь, какие сценарии он реально проверяет, а какие нет;
3) выбираешь мутацию по строгой иерархии приоритетов (test-gap → logical-violation →
   developer-mistake → primitive) и не скатываешься в тривиальные замены без необходимости;
4) никогда не подгоняешь мутацию под существующие assert-ы теста.
Ты всегда отвечаешь строго в соответствии с заданной JSON-схемой и больше ничего не пишешь.`

// MutationOperatorNames — фиксированный набор категорий мутаций (operator_name).
// Это «что именно сломано» в терминах паттернов Go-кода.
var MutationOperatorNames = []string{
	"ContextPropagation",
	"StateConsistency",
	"ResourceLifecycle",
	"DefaultFallback",
	"ErrorContract",
	"ParsingDelimiter",
	"ProtocolInvariant",
	"CollectionSemantics",
	"IdentifierConfusion",
	"BoundaryInvariant",
	"BooleanPredicate",
	"NilEmptySemantics",
}

// MutationKindNames — фиксированный набор приоритетных уровней мутации
// в порядке убывания ценности. Соответствует mutator.Kind.
var MutationKindNames = []string{
	"test-gap",
	"logical-violation",
	"developer-mistake",
	"primitive",
}

// MutationJSONSchema — JSON Schema для Structured Output.
// Используется с моделями, поддерживающими режим Structured Output.
var MutationJSONSchema = map[string]any{
	"type": "object",
	"properties": map[string]any{
		"mutation_kind": map[string]any{
			"type":        "string",
			"enum":        MutationKindNames,
			"description": "Приоритетный уровень мутации: test-gap | logical-violation | developer-mistake | primitive. Выбирается по иерархии из промпта — бери первый подходящий уровень.",
		},
		"operator_name": map[string]any{
			"type":        "string",
			"enum":        MutationOperatorNames,
			"description": "Категория мутации из фиксированного списка",
		},
		"description": map[string]any{
			"type":        "string",
			"description": "Одно предложение, объясняющее внесённый дефект. Если mutation_kind=test-gap, явно назови сам пробел в тесте.",
		},
		"behavioral_impact": map[string]any{
			"type":        "string",
			"description": "Конкретный сценарий: для входа или вызова X оригинал делает Y, а мутант делает Z. Если такого примера нет — мутация эквивалентна и её нельзя использовать.",
		},
		"original_snippet": map[string]any{
			"type":        "string",
			"description": "Точная и по возможности уникальная подстрока из исходного кода, которую нужно заменить",
		},
		"mutated_snippet": map[string]any{
			"type":        "string",
			"description": "Текст замены — синтаксически корректный Go-код",
		},
	},
	"required":             []string{"mutation_kind", "operator_name", "description", "behavioral_impact", "original_snippet", "mutated_snippet"},
	"additionalProperties": false,
}

// BuildPrompt формирует пользовательский промпт с явным chain-of-thought
// алгоритмом выбора мутации по четырём приоритетам.
//
// Параметр useStructuredOutput управляет тем, дублируется ли описание JSON-формата
// внутри промпта (для моделей без Structured Output API).
// testBody — исходный код теста, покрывающего функцию (может быть пустым).
func BuildPrompt(funcBody, fileName string, lineHint int, useStructuredOutput bool, testBody string) string {
	operatorList := strings.Join(MutationOperatorNames, ", ")
	kindList := strings.Join(MutationKindNames, ", ")

	formatInstructions := ""
	if !useStructuredOutput {
		formatInstructions = fmt.Sprintf(`
## Формат ответа
Верни ТОЛЬКО валидный JSON без markdown-разметки:
{
  "mutation_kind":     "<один из: %s>",
  "operator_name":     "<один из: %s>",
  "description":       "<одно предложение об ошибке; для test-gap — назови пробел теста>",
  "behavioral_impact": "<для входа/вызова X оригинал делает Y, мутант делает Z>",
  "original_snippet":  "<точная подстрока для замены>",
  "mutated_snippet":   "<текст замены>"
}
`, kindList, operatorList)
	}

	testSection := ""
	if testBody != "" {
		testSection = fmt.Sprintf(`
## Тест, покрывающий функцию
Используй этот тест как контекст ожидаемого поведения и текущего покрытия.
Не подгоняй мутацию под конкретные assert-ы теста и не выбирай искусственную
замену только ради того, чтобы тест её пропустил. Цель — оценить тест, а не его обмануть.

Что нужно сделать:
- определи, какие входы и ветки реально покрываются;
- ищи КРИТИЧЕСКИЕ пробелы: непроверенные ветки ошибок, отсутствие edge case,
  смешение nil vs empty, не покрытые границы, отсутствующая валидация
  контракта, пропущенный альтернативный сценарий;
- если такой пробел явно виден — это приоритет №1 (см. иерархию ниже),
  и в description прямо назови пробел проверки;
- если явного пробела нет — не выдумывай его и не утверждай, что тест
  обнаружит или не обнаружит мутанта; переходи к следующему приоритету.

%s
`, testBody)
	}

	return fmt.Sprintf(`Ты — эксперт по тестированию Go-кода.
Сгенерируй ОДНУ реалистичную мутацию для функции ниже.
Мутация должна имитировать реальную логическую ошибку разработчика, а не случайную замену токена.

## Шаг 1. Анализ кода
Перед выбором мутации мысленно ответь себе:
- Какова предметная область функции и её смысл в большей системе?
- Какие у функции инварианты, контракты, граничные условия и побочные эффекты?
- Какие у неё критические ветки (ошибки, fallback, lifecycle, разные типы входа)?
Только после этого ты сможешь придумать доменно осмысленные ошибки, а не подменить токен наугад.

## Шаг 2. Анализ теста
Если ниже передан тест — следуй инструкциям в секции с его телом
(определи покрытые ветки, найди критические пробелы покрытия, не выдумывай их).
Если теста нет — переходи к шагу 3, начиная с приоритета 2.

## Шаг 3. Выбор мутации по приоритету
Иди по уровням сверху вниз и используй ПЕРВЫЙ подходящий.
Заполни поле "mutation_kind" соответствующим значением.

### Приоритет 1 — "test-gap"
Используй, ЕСЛИ из теста явно виден критический пробел покрытия.
Сгенерируй мутацию, которая:
- остаётся корректной на ВСЕХ покрытых тестом сценариях;
- даёт неправильный результат именно в НЕПОКРЫТОМ сценарии.
Так выживший мутант доказывает: тест слабый и его нужно расширить.
В "description" обязательно назови сам пробел: "Тест не проверяет <ветка/edge case>".

### Приоритет 2 — "logical-violation"
Используй, если критического пробела не видно (или теста нет).
Сломай ВАЖНЫЙ доменный инвариант или контракт функции. Примеры:
- неверный ключ/поле/состояние при обновлении или поиске (StateConsistency, IdentifierConfusion);
- нарушенный жизненный цикл ресурса: открыл — не закрыл, lock — не unlock, defer перед операцией (ResourceLifecycle);
- неверный fallback или контракт обработки ошибок: проглотить error, вернуть nil вместо ошибки (DefaultFallback, ErrorContract);
- потерянный контекст или передача неподходящего ctx (ContextPropagation);
- неправильное правило парсинга, маршрутизации или протокольный инвариант (ParsingDelimiter, ProtocolInvariant);
- перепутанная семантика nil vs empty, owned vs borrowed, value vs pointer (NilEmptySemantics, CollectionSemantics).
Это самые ценные мутанты — они проверяют, что тест защищает доменный смысл, а не отдельные строки.

### Приоритет 3 — "developer-mistake"
Используй, если для шагов 1-2 не нашлось подходящего инварианта.
Имитируй наиболее вероятную ошибку разработчика, которую видно по контексту:
- off-by-one на границе цикла, индексе, сравнении (BoundaryInvariant);
- перепутанные имена похожих переменных/полей/аргументов: a vs b, src vs dst, in vs out, want vs got (IdentifierConfusion);
- неверный порядок шагов: например defer mu.Unlock() до mu.Lock(), close до использования;
- неверная константа из набора близких: http.StatusOK вместо StatusCreated, time.Second вместо time.Millisecond;
- инвертированный предикат там, где это правдоподобная опечатка (BooleanPredicate).
Эти мутации тоже доменно осмысленные, но более «механические», чем приоритет 2.

### Приоритет 4 — "primitive" (только если все предыдущие не подходят)
Допустима тривиальная замена оператора/литерала ТОЛЬКО при двух условиях одновременно:
- ты не смог найти ни test-gap, ни logical-violation, ни developer-mistake;
- в "behavioral_impact" ты явно объясняешь, какой именно доменный инвариант ломается этой заменой.

## Технические правила
- Мутируй ТОЛЬКО ОДНО место в функции.
- Не добавляй импорты и не меняй сигнатуру функции.
- "original_snippet" — точная, по возможности уникальная подстрока (выражение, оператор присваивания, строка или небольшой блок).
  Это должно быть осмысленное выражение, а не одиночный токен/оператор/литерал вроде "1", "0", "<", "<=", "&&", "||", "true", "false" или "nil".
- "mutated_snippet" — синтаксически корректный Go-код, замещающий original_snippet.
- "operator_name" — строго одна из категорий: %s.
- "mutation_kind" — строго одно из значений: %s.
- Сохраняй компилируемость и базовую vet-корректность пакета:
  не оставляй неиспользуемые импорты или переменные, не создавай undefined identifiers,
  не удаляй единственное использование локальной переменной, если её объявление остаётся,
  и не создавай очевидно невозможные условия (x == A && x == B при разных константах A и B).

## Запреты
- Мутант делает код хуже, а не лучше. Запрещено всё, что выглядит как исправление:
  добавление недостающей инициализации, улучшение синхронизации map/slice, более строгая
  валидация, безопасный fallback, cleanup, оптимизация или рефакторинг.
- Не выбирай классический оператор мутации как самоцель. Замены вида "<" на "<=",
  "&&" на "||", "1" на "0" допустимы только в рамках приоритетов 3-4 с обоснованием в behavioral_impact.
- Не подгоняй мутацию под существующий тест — ни под его assert-ы, ни под отсутствие assert-ов.
- Избегай искусственных значений, debug/test-суффиксов, бессмысленных литералов.
- Не заменяй значение на nil, 0, false, пустую строку или пустой объект, если это просто
  ломает happy path. Используй такие замены только когда это правдоподобная ошибка в
  конкретной ветке fallback / error / default handling.
- Избегай crash-only мутантов, основной эффект которых — немедленная паника на happy path.
  Не обнуляй конструкторы, обязательные инициализаторы, pool.New, context pointers или
  обязательные поля структуры ради nil-dereference. Паника допустима только если мутируется
  ветка, чей контракт уже связан с panic/error handling.
- ЗАПРЕЩЕНО генерировать семантически эквивалентные мутации. Эквивалентная мутация — та,
  при которой функция возвращает тот же результат на ВСЕХ возможных входах. Например, в
  функции Max(a,b) замена "a > b" на "a >= b" эквивалентна: при a==b обе версии возвращают одно.
- Перед ответом мысленно проверь: существует ли хотя бы один конкретный входной набор,
  на котором мутированная версия вернёт ДРУГОЙ результат? Если нет — выбери другое место.

## Формат behavioral_impact
Обязательно: "Для входа/вызова X оригинал делает Y, мутант делает Z".
Если "mutation_kind" = "test-gap", добавь: "Пробел теста: не проверяется <input/ветка/edge case>".
%s%s
## Функция для мутации
// Файл: %s  (область — строка %d)
%s
`, operatorList, kindList, formatInstructions, testSection, fileName, lineHint, funcBody)
}
